package com.example.ahmadsheraz.worldanimecommunity.ModelClasses;

public class appTheme {

    int fullName;
    int username;
    int post;
    int imgheart;
    int imgmessage;
    int imgpoo;
    int imgpopUp;
    int backGround;
    int postTime;

    public int getFullName() {
        return fullName;
    }

    public void setFullName(int fullName) {
        this.fullName = fullName;
    }

    public int getUsername() {
        return username;
    }

    public void setUsername(int username) {
        this.username = username;
    }

    public int getPost() {
        return post;
    }

    public void setPost(int post) {
        this.post = post;
    }

    public int getImgheart() {
        return imgheart;
    }

    public void setImgheart(int imgheart) {
        this.imgheart = imgheart;
    }

    public int getImgmessage() {
        return imgmessage;
    }

    public void setImgmessage(int imgmessage) {
        this.imgmessage = imgmessage;
    }

    public int getImgpoo() {
        return imgpoo;
    }

    public void setImgpoo(int imgpoo) {
        this.imgpoo = imgpoo;
    }

    public int getImgpopUp() {
        return imgpopUp;
    }

    public void setImgpopUp(int imgpopUp) {
        this.imgpopUp = imgpopUp;
    }

    public int getBackGround() {
        return backGround;
    }

    public void setBackGround(int backGround) {
        this.backGround = backGround;
    }

    public int getPostTime() {
        return postTime;
    }

    public void setPostTime(int postTime) {
        this.postTime = postTime;
    }

    public appTheme(int fullName, int username, int post, int imgheart, int imgmessage, int imgpoo, int imgpopUp, int backGround, int postTime) {
        this.fullName = fullName;
        this.username = username;
        this.post = post;
        this.imgheart = imgheart;
        this.imgmessage = imgmessage;
        this.imgpoo = imgpoo;
        this.imgpopUp = imgpopUp;
        this.backGround = backGround;
        this.postTime = postTime;
    }

}
